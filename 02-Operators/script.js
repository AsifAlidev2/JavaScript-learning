// Operators are used to perform some operations

let a = 8;
let b = 5;


// + used for addition
console.log("Addition:", a + b);       // Addition: 13

// - used for subtraction
console.log("Subtraction:", a - b);    // Subtraction: 3

// * used for Multiplication
console.log("Multiplication:", a * b); // Multiplication: 40

// / used for Division 
console.log("Division:", a / b);       // Division: 1.6

// % used to find remainder

console.log("Remainder:", a % b);      // Remainder: 3
